#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	while (1)
	{
		if (i * i == nb)
			return (i);
		else if ((i - 1) * (i - 1) < nb && i * i > nb)
			return (0);
		i++;
	}
}
