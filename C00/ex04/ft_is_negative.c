#include <unistd.h>

void	ft_is_negative(int n)
{
	char	pos;
	char	neg;

	neg = 'N';
	pos = 'P';
	if (n >= 0)
	{
		write(1, &pos, 1);
	}
	if (n < 0)
	{
		write(1, &neg, 1);
	}
}
